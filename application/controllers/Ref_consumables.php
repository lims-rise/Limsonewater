use PhpOffice\PhpSpreadsheet\Spreadsheet;
use PhpOffice\PhpSpreadsheet\Writer\Xlsx;
    
class Ref_consumables extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        is_login();
        $this->load->model('Ref_consumables_model');
        $this->load->library('form_validation');        
	    $this->load->library('datatables');
	    $this->load->library('uuid');
    }

    public function index()
    {
        $data['stockName'] = $this->Ref_consumables_model->getStock();
        $data['objectives'] = $this->Ref_consumables_model->getObjective();
        $this->template->load('template','ref_consumables/index', $data);
    } 
    
    public function json() {
        header('Content-Type: application/json');
        echo $this->Ref_consumables_model->json();
    }

    public function save() 
    {
        $mode = $this->input->post('mode',TRUE);
        $id = $this->input->post('id_consumables',TRUE);
        $id_stock = $this->input->post('id_stock',TRUE);
        $id_objective = $this->input->post('id_objective',TRUE);
        $dt = new DateTime();

        if ($mode=="insert"){
            $data = array(
            'id_objective' => $id,
            'id_stock' => $id_stock,
            'id_objective' => $id_objective,
            'uuid' => $this->uuid->v4(),
            'flag' => '0',
            'lab' => $this->session->userdata('lab'),
            'user_created' => $this->session->userdata('id_users'),
            'date_created' => $dt->format('Y-m-d H:i:s'),
            );

            // var_dump($data);
            // die();
 
            $this->Ref_consumables_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');    
        }
        else if ($mode=="edit"){
            $data = array(
            'id_objective' => $id,
            'id_stock' => $id_stock,
            'id_objective' => $id_objective,
            'uuid' => $this->uuid->v4(),
            'flag' => '0',
            'lab' => $this->session->userdata('lab'),
            'user_updated' => $this->session->userdata('id_users'),
            'date_updated' => $dt->format('Y-m-d H:i:s'),
            );

            $this->Ref_consumables_model->update($id, $data);
            $this->session->set_flashdata('message', 'Create Record Success');    
        }

        redirect(site_url("ref_consumables"));
    }

    public function delete($id) 
    {
        $row = $this->Ref_consumables_model->get_by_id($id);
        $data = array(
            'flag' => 1,
            );

        if ($row) {
            $this->Ref_consumables_model->update($id, $data);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('ref_consumables'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('ref_consumables'));
        }
    }

    public function valid_bs() 
    {
        $id = $this->input->get('id1');
        $data = $this->Ref_consumables_model->validate1($id);
        header('Content-Type: application/json');
        echo json_encode($data);
    }


    public function excel() {
    
        $spreadsheet = new Spreadsheet();    
        $sheet = $spreadsheet->getActiveSheet();
        $sheet->setCellValue('A1', "ID"); 
        $sheet->setCellValue('B1', "Product Name"); 
        $sheet->setCellValue('C1', "Objective"); 

        $ref_consumables = $this->Ref_consumables_model->get_ref_consumables();
    
        $numrow = 2;
        foreach($ref_consumables as $data){ 
            if (property_exists($data, 'id_consumables')) {
                $sheet->setCellValue('A'.$numrow, $data->id_consumables);
            } else {
                $sheet->setCellValue('A'.$numrow, '');
            }
            if (property_exists($data, 'product_name')) {
                $sheet->setCellValue('B'.$numrow, $data->product_name);
            } else {
                $sheet->setCellValue('B'.$numrow, '');
            }
    
            if (property_exists($data, 'objective')) {
                $sheet->setCellValue('C'.$numrow, $data->objective);
            } else {
                $sheet->setCellValue('C'.$numrow, '');
            }

            $numrow++;
        }

        // Set header untuk file excel
        header('Content-Type: application/vnd.openxmlformats-officedocument.spreadsheetml.sheet');
        header('Content-Disposition: attachment;filename="Report_ref_consumables.xlsx"');
        header('Cache-Control: max-age=0');

        // Tampilkan file excel
        $writer = new Xlsx($spreadsheet);
        $writer->save('php://output');
    }
}

